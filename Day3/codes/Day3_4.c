#include <stdio.h>
int main()
{



    //datatypes => int , double , char
    int num = 10;//variable declaration + initialization 
    printf("%d\n", num);//10
    num = 200; //assignment   
    printf("%d\n", num);//200 
    num = -1; 
    num = 20; 
    printf("%d\n", num);//20  
    num = 30; 
    num = 1000;  
    printf("%d\n", num);//1000 
    
    //assignment can be done multiple number of times 
    //initialization can be done only once  

    return 0;
}
